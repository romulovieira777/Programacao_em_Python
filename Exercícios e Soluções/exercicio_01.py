"""
Ler dois números inteiros, executar e mostrar o resultado das seguintes operações:
adição, subtração, multiplicação e divisão.
"""

# Criando Duas Variáveis
number_01 = int(input('Enter the first number: '))
number_02 = int(input('Enter the second number: '))

# Somando as Duas Variáveis e Imprimindo na Tela
print('A soma é', number_01 + number_02)


# Subtraindo as Duas Variáveis e Imprimindo na Tela
print('A subtração é', number_01 - number_02)


# Dividindo as Duas Variáveis e Imprimindo na Tela
print('A divisão é', number_01 / number_02)


# Mutiplicando as Duas Variáveis e Imprimindo na Tela
print('A multiplicação é', number_01 * number_02)
