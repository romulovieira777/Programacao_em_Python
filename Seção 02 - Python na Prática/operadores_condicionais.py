# Criando uma Estrutura de Condição com if e else
if n == 4:
    # Imprimindo o Valor na Tela se a Primeira Condição for Verdadeira
    print('n é igual a 4')
# Se a Primeira Condição não for Verdadeira Excuta o Else
else:
    # Verificando se a Variável N é Igual a 3
    if n == 3:
        # Se a Primeira Condição for Verdadeira Imprimi o Valor na tela
        print('n é igual a 3')
    # Se a Primeira Condição for Falsa Executa o Else
    else:
        # Imprimindo o Valor na Tela se a Condição for Verdadeira
        print('n não é igual a 4 nem 3')


