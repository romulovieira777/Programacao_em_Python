# Criando Duas Variáveis de Operadores Lógicos
a = True
b = False

# Imprimindo as Duas Variáveis na Tela
print(a, b)


# Criando uma Variável
c = a and b

# Imprimindo na Tela o Resultado das Variáveis
print("'A' e 'B' são iguais é", c)


# Criando uma Variável
d = a or b

# Imprimindo na Tela o Resultado das Variáveis
print("'A' ou 'B' é igual a", d)


# Criando uma Variável
e = not b

# Imprimindo na Tela o Resultado das Variáveis
print('A negacação de B é:', e)
